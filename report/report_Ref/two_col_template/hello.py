def main():

    # print the text in quotes
    print('Hello World')

    # print the text in quotes 10 times
    for i in range(10):
        print('Hi')

    # print the numbers 0 to 9
    for i in range(10):
        print(i)

    # print the numbers 1 to 10
    for i in range(10):
        print(i+1)

    # another way to do this
    for i in range(1,11):
        print(i)

        
if __name__ == "__main__":
    main()
